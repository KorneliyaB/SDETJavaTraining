package class9.HomeWork;

public class ReverseOrder {
    //Create an array to store char values and then print
    // those in reverse order
    public static void main(String[] args) {
        char [] index={'a','b','c'};
        for (int i=2; i>=0; i--){
            System.out.println(index[i]+" ");
        }

    }
}
