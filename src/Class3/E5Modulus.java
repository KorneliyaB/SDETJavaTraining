package Class3;

public class E5Modulus {
    public static void main(String[] args) {

        int number1=-21;
        int number2=4;
        // devision shows how many the result
        System.out.println(number1/number2);
        // what left
        System.out.println(number1%number2);

        int noOfSlices=4;
        int noOfPeople=2;
        //devision shows how many the result how many slices get each person
        System.out.println(noOfSlices/noOfPeople);
        // number of slices left after even devision
        System.out.println(noOfSlices%noOfPeople);
    }
}
